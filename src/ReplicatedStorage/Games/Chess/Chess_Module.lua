--────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────
function Chess_Module.GetPieceColor(PieceCode)
    if PieceCode ~= Piece.None then
        if PieceCode > 8 then
            if PieceCode < Piece.Black then
                return(Piece.White)
            else
                return(Piece.Black)
            end
        end
    end
    return(nil)
end
